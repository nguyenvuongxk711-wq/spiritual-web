/* Mốc: Mùng 1 Tết âm lịch 1900 = 31/01/1900 dương lịch */
const EPOCH = new Date(1900, 0, 31);

function daysBetween(a: Date, b: Date): number {
  const ms = 1000 * 60 * 60 * 24;
  return Math.floor((a.getTime() - b.getTime()) / ms);
}

/* Tìm năm âm, tháng âm, ngày âm từ số ngày từ epoch */
function getLunarDate(days: number): { year: number; month: number; day: number; isLeap: boolean } {
  let remaining = days;
  for (let y = 0; y < LUNAR_DATA.length; y++) {
    const yearData = LUNAR_DATA[y];
    const leapMonth = yearData[12] || 0;
    const totalDays = yearData.slice(0, 12).reduce((a, b) => a + b, 0) + (leapMonth > 0 ? yearData[leapMonth - 1] : 0);
    if (remaining < totalDays) {
      let monthDays = 0;
      for (let m = 0; m < 12; m++) {
        const md = yearData[m];
        if (remaining < md) {
          return { year: 1900 + y, month: m + 1, day: remaining + 1, isLeap: false };
        }
        remaining -= md;
        monthDays += md;
        if (leapMonth === m + 1) {
          const leapDays = yearData[m]; /* tháng nhuận có cùng số ngày với tháng trước */
          if (remaining < leapDays) {
            return { year: 1900 + y, month: m + 1, day: remaining + 1, isLeap: true };
          }
          remaining -= leapDays;
          monthDays += leapDays;
        }
      }
      return { year: 1900 + y, month: 12, day: remaining + 1, isLeap: false };
    }
    remaining -= totalDays;
  }
  return { year: 1900, month: 1, day: 1, isLeap: false };
}

/* ========== ĐỔI DƯƠNG → ÂM ========== */
export function solarToLunar(date: Date): { year: number; month: number; day: number; isLeap: boolean; canChi: string } {
  const days = daysBetween(date, EPOCH);
  const lunar = getLunarDate(days);
  const canChi = getCanChiYear(lunar.year);
  return { ...lunar, canChi: `${canChi.can} ${canChi.chi}` };
}

/* ========== ĐỔI ÂM → DƯƠNG ========== */
export function lunarToSolar(lunarYear: number, lunarMonth: number, lunarDay: number, isLeap: boolean = false): Date {
  let days = 0;
  for (let y = 0; y < lunarYear - 1900; y++) {
    const yearData = LUNAR_DATA[y];
    const leapMonth = yearData[12] || 0;
    days += yearData.slice(0, 12).reduce((a, b) => a + b, 0) + (leapMonth > 0 ? yearData[leapMonth - 1] : 0);
  }
  const yearData = LUNAR_DATA[lunarYear - 1900];
  const leapMonth = yearData[12] || 0;
  for (let m = 0; m < lunarMonth - 1; m++) {
    days += yearData[m];
    if (leapMonth === m + 1) {
      days += yearData[m];
    }
  }
  if (isLeap && leapMonth === lunarMonth) {
    days += yearData[lunarMonth - 1];
  }
  days += lunarDay - 1;
  return new Date(EPOCH.getTime() + days * 86400000);
}

/* ========== CAN CHI ========== */
export function getCanChiYear(year: number): { can: string; chi: string } {
  const idx = ((year - 4) % 60 + 60) % 60;
  return { can: CAN[idx % 10], chi: CHI[idx % 12] };
}

export function getCanChiMonth(year: number, month: number): { can: string; chi: string } {
  const yearCan = getCanChiYear(year).can;
  const canStart: Record<string, number> = { Giáp:2, Ất:2, Bính:3, Đinh:3, Mậu:4, Kỷ:4, Canh:5, Tân:5, Nhâm:6, Quý:6 };
  const m = (canStart[yearCan] + month - 1) % 10;
  return { can: CAN[m], chi: CHI[(month + 1) % 12] };
}

export function getCanChiDay(date: Date): { can: string; chi: string } {
  const epoch = new Date(1900, 0, 1);
  const diff = daysBetween(date, epoch);
  const idx = ((16 + diff) % 60 + 60) % 60;
  return { can: CAN[idx % 10], chi: CHI[idx % 12] };
}

export function getCanChiHour(dayCan: string, hour: number): { can: string; chi: string } {
  const canStart: Record<string, number> = { Giáp:0, Ất:0, Bính:2, Đinh:2, Mậu:4, Kỷ:4, Canh:6, Tân:6, Nhâm:8, Quý:8 };
  const h = Math.floor((hour + 1) / 2) % 12;
  const c = (canStart[dayCan] + h) % 10;
  return { can: CAN[c], chi: CHI[h] };
}

/* ========== TỨ TRỤ CHÍNH XÁC ========== */
export function getTuTruFull(date: Date): { nam: string; thang: string; ngay: string; gio: string } {
  const solar = solarToLunar(date);
  const ccYear = getCanChiYear(solar.year);
  const ccMonth = getCanChiMonth(solar.year, solar.month);
  const ccDay = getCanChiDay(date);
  const hour = date.getHours();
  const ccHour = getCanChiHour(ccDay.can, hour);
  return {
    nam: `${ccYear.can} ${ccYear.chi}`,
    thang: `${ccMonth.can} ${ccMonth.chi}`,
    ngay: `${ccDay.can} ${ccDay.chi}`,
    gio: `${ccHour.can} ${ccHour.chi}`,
  };
}

