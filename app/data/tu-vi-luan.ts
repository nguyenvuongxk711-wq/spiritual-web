export function luanCungChiTiet(cungName: string, stars: string[]): string {
  for (const star of stars) {
    if (L[star]?.[cungName]) return L[star][cungName];
  }
  return `${cungName}: Cung này không có chính tinh nổi bật. Vận mệnh trung bình, không có biến động lớn. Nên tập trung vào công việc chăm chỉ, đạt được thành công từ sự nỗ lực.`;
}

